class Solution
{
    public:
    //Function to find length of longest increasing subsequence.
    int longestSubsequence(int n, int a[])
    {
       // your code here
       set<int> s;
       for(int i=0;i<n;i++){
           s.insert(a[i]);
       }
       vector<int> ans(s.begin(),s.end());
       vector<vector<int>> dp(n+1,vector<int>(ans.size()+1,0));
        
        for(int i=1;i<=n;i++){
            for(int j=1;j<=ans.size();j++){
                if(a[i-1]==ans[j-1]){
                    dp[i][j] = 1 + dp[i-1][j-1];
                }
                else{
                    dp[i][j] = max(dp[i-1][j],dp[i][j-1]);
                }
            }
        }
        return dp[n][ans.size()];
    }
};

